/** Minimum rect area to keep — filters out tiny ~27×27 squares. */
const MIN_BLOCK_AREA = 1000

function isPath(el: SvgElement): el is PathElement {
  return 'tag' in el && el.tag === 'path'
}

const FILTERED_INDICES: number[] = []
for (let i = 0; i < SVG_ELEMENTS.length; i++) {
  const el = SVG_ELEMENTS[i]
  if (isPath(el)) {
    FILTERED_INDICES.push(i)
    continue
  }
  if (Number.parseFloat(el.width) * Number.parseFloat(el.height) >= MIN_BLOCK_AREA) {
    FILTERED_INDICES.push(i)
  }
}

function wrap(v: number, m: number) {
  return ((v % m) + m) % m
}

/**
 * Full-perimeter decorative block frame using the Figma SVG export.
 *
 * Animation runs entirely via rAF + direct DOM mutation (no React re-renders).
 * Only blocks above MIN_BLOCK_AREA are rendered to reduce DOM node count.
 */
interface ChangelogBlocksProps {
  mode?: 'time' | 'scroll'
}

export function ChangelogBlocks({ mode = 'time' }: ChangelogBlocksProps) {
  const elemsRef = useRef<(SVGRectElement | SVGPathElement | null)[]>([])

  useEffect(() => {
    const applyOpacities = (progress: number) => {
      const windowSize = PERIMETER * WINDOW_FRAC
      const headDist = wrap(progress, 1) * PERIMETER
      const tailDist = headDist - windowSize

      for (let j = 0; j < FILTERED_INDICES.length; j++) {
        const idx = FILTERED_INDICES[j]
        const [perimDist, baseOpacity] = BLOCK_ANIM[idx]
        const distFromTail = wrap(perimDist - tailDist, PERIMETER)
        const visible = distFromTail <= windowSize
        const node = elemsRef.current[j]
        if (node) node.setAttribute('opacity', visible ? String(baseOpacity) : '0')
      }
    }

    if (mode === 'scroll') {
      let frame = 0
      let lastScrollY = window.scrollY
      let scrollAccumulatorPx = 0
      let lastProgress = 0

      const update = () => {
        frame = 0
        const nextScrollY = window.scrollY
        const deltaY = nextScrollY - lastScrollY
        lastScrollY = nextScrollY
        if (deltaY === 0) return

        scrollAccumulatorPx += deltaY
        const nextProgress = scrollAccumulatorPx / SCROLL_CYCLE_PX
        if (Math.abs(nextProgress - lastProgress) < 0.001) return

        lastProgress = nextProgress
        applyOpacities(nextProgress)
      }

      const onScroll = () => {
        if (frame) return
        frame = window.requestAnimationFrame(update)
      }

      const onResize = () => {
        lastScrollY = window.scrollY
      }

      window.addEventListener('scroll', onScroll, { passive: true })
      window.addEventListener('resize', onResize)

      return () => {
        window.removeEventListener('scroll', onScroll)
        window.removeEventListener('resize', onResize)
        if (frame) window.cancelAnimationFrame(frame)
      }
    }

    let frame: number
    const start = performance.now()
    const tick = (now: number) => {
      applyOpacities((now - start) / LOOP_DURATION_MS)
      frame = requestAnimationFrame(tick)
    }
    frame = requestAnimationFrame(tick)
    return () => cancelAnimationFrame(frame)
  }, [mode])

  return (
    <div
      aria-hidden='true'
      className='pointer-events-none absolute inset-0 z-0 hidden overflow-hidden lg:block'
    >
      <svg
        viewBox={`0 0 ${SVG_W} ${SVG_H}`}
        preserveAspectRatio='none'
        className='h-full w-full'
        fill='none'
        xmlns='http://www.w3.org/2000/svg'
      >
        {FILTERED_INDICES.map((i, j) => {
          const el = SVG_ELEMENTS[i]
          const setRef = (node: SVGRectElement | SVGPathElement | null) => {
            elemsRef.current[j] = node
          }
          if (isPath(el)) {
            return <path key={i} ref={setRef} d={el.d} fill={remapFill(el.fill)} opacity='0' />
          }
          return (
            <rect
              key={i}
              ref={setRef as React.Ref<SVGRectElement>}
              x={el.x}
              y={el.y}
              width={el.width}
              height={el.height}
              rx={el.rx}
              fill={remapFill(el.fill)}
              transform={el.transform}
              opacity='0'
            />
          )
        })}
      </svg>
    </div>
  )
}
